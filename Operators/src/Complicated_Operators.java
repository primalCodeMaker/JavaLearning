public class Complicated_Operators {

    public static void main(String[] args) {
        int a = 3;
        a += 2;     // a= a + 2
        System.out.println(a);

        int b = 9;
        b -= 1;     // b = b - 1
        System.out.println(b);

        int c = 10;
        c *= 2;     // c = c * 2
        System.out.println(c);

        int d = 100;
        d /= 4;     // d = d / 4
        System.out.println(d);

        int e = 7;
        e %= 2;     //e = e % 2
        System.out.println(e);

    }

}
